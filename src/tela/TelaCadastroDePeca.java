    private void loadTablePecas() throws Exception {
        try {
            ArrayList<Peca> listaPecas = new ManipulaBancoPecas().buscarTodos();
            String[][] dados = new String[listaPecas.size()][3];
            for (int i = 0; i < listaPecas.size(); i++) {
                dados[i][0] = listaPecas.get(i).getCodigoPeca();
                dados[i][1] = listaPecas.get(i).getDescricao();
                dados[i][2] = Float.toString(listaPecas.get(i).getValorPeca());
            }
            
            jTablePecasCadastradas.setModel(new DefaultTableModel(dados, new Object[]{"CÓDIGO", "NOME", "VALOR"}));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

